app.post('/api/auth/reset-password', async (req, res) => {
  const { email, otp, newPassword } = req.body;
  const record = otpStore[email];
  
  if (!record || record.otp !== otp || record.expires < Date.now()) {
    return res.status(400).json({ error: 'Invalid or expired OTP' });
  }

  try {
    const hashedPw = await bcrypt.hash(newPassword, 10);
    db.run(`UPDATE users SET password_hash = ? WHERE email = ?`, [hashedPw, email], function(err) {
      if (err) return res.status(500).json({ error: 'Database update failed' });
      delete otpStore[email];
      res.json({ success: true });
    });
  } catch(e) {
    res.status(500).json({ error: 'Failed to reset password' });
  }
});

// ─── LOGS ROUTES ─────────────────────────────────────────────────────────────
app.get('/api/logs/:userId', authenticateToken, (req, res) => {
  db.all(`SELECT * FROM logs WHERE user_id = ? ORDER BY date ASC`, [req.params.userId], (err, rows) => {
    if (err) return res.status(500).json({ error: 'DB Error' });
    const formatted = rows.map(r => ({ ...r, symptoms: JSON.parse(r.symptoms || '[]') }));
    res.json(formatted);
  });
});

app.get('/api/log/:userId/:date', authenticateToken, (req, res) => {
  db.get(`SELECT * FROM logs WHERE user_id = ? AND date = ?`, [req.params.userId, req.params.date], (err, row) => {
    if (err || !row) return res.json(null);
    res.json({ ...row, symptoms: JSON.parse(row.symptoms || '[]') });
  });
});

app.post('/api/logs/:userId', authenticateToken, (req, res) => {
  const { date, flowLevel, painLevel, moodLevel, symptoms, notes, bloodColor, clotting, energyLevel, discharge } = req.body;
  const userId = req.params.userId;
  const id = userId + "_" + date;
  const savedAt = new Date().toISOString();
  const symptomsJson = JSON.stringify(symptoms || []);

  db.run(
    `INSERT OR REPLACE INTO logs (id, user_id, date, flowLevel, painLevel, moodLevel, symptoms, notes, bloodColor, clotting, energyLevel, discharge, saved_at) 
     VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`,
    [id, userId, date, flowLevel, painLevel, moodLevel, symptomsJson, notes, bloodColor, clotting, energyLevel, discharge, savedAt],
    (err) => {
      if (err) { console.error(err); return res.status(500).json({ error: 'Failed to save log' }); }
      res.json({ success: true, id });
    }
  );
});

