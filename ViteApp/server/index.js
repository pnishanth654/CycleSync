app.post('/api/auth/register', async (req, res) => {
  const { name, dob, email, mobile, password, otp } = req.body;
  
  if (email && otp) {
    const record = otpStore[email];
    if (!record || record.otp !== otp || record.expires < Date.now()) {
      return res.status(400).json({ error: 'Invalid or expired OTP' });
    }
    delete otpStore[email];
  }

  try {
    const hashedPw = await bcrypt.hash(password, 10);
    const id = uuidv4();
    const createdAt = new Date().toISOString();

    db.run(
      `INSERT INTO users (id, name, dob, email, mobile, password_hash, created_at) VALUES (?, ?, ?, ?, ?, ?, ?)`,
      [id, name, dob, email, mobile, hashedPw, createdAt],
      function (err) {
        if (err) return res.status(400).json({ error: 'Email already exists or DB error' });
        const user = { id, name, dob, email, mobile, gender: 'female', createdAt };
        const token = jwt.sign({ userId: id }, process.env.JWT_SECRET, { expiresIn: '30d' });
        res.json({ user, token });
      }
    );
  } catch (err) {
    res.status(500).json({ error: 'Registration failed' });
  }
});

app.post('/api/auth/login', async (req, res) => {
  const { email, password } = req.body;
  if (!email || !password) return res.status(400).json({ error: 'Email and password required' });

  db.get(`SELECT * FROM users WHERE email = ?`, [email], async (err, user) => {
    if (err || !user) return res.status(400).json({ error: 'Invalid email or user not found' });
    
    const valid = await bcrypt.compare(password, user.password_hash);
    if (!valid) return res.status(400).json({ error: 'Invalid password' });
    
    const token = jwt.sign({ userId: user.id }, process.env.JWT_SECRET, { expiresIn: '30d' });
    return res.json({ user, token });
  });
});

